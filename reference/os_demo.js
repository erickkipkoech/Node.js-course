const os=require('os');

//Show file platform
console.log(os.platform());

//Cpu architecture
console.log(os.arch());

//cpu core info
console.log(os.cpus());

//free memory
console.log(os.freemem());

//Total memory
console.log(os.totalmem());

//Home directory
console.log(os.homedir());

//uptime
console.log(os.uptime());
